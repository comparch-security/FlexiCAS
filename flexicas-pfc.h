#ifndef CM_FLEXICAS_PFC_HPP
#define CM_FLEXICAS_PFC_HPP

#define FLEXICAS_PFC_ADDR          0x00ffffffffffffffull
#define FLEXICAS_PFC_ADDR_SIGN     0x0080000000000000ull
#define FLEXICAS_PFC_ADDR_NEGF     0xff00000000000000ull

#define FLEXICAS_PFC_CMD           0x8000000000000000ull
#define FLEXICAS_PFC_CMD_MASK      0x000000000000ffffull
#define FLEXICAS_PFC_START         0x0000ull
#define FLEXICAS_PFC_STOP          0x0001ull
#define FLEXICAS_PFC_STR_CHAR      0x0002ull
#define FLEXICAS_PFC_STR_CLR       0x0003ull
#define FLEXICAS_PFC_PREFIX        0x0004ull

#define FLEXICAS_PFC_FENCE         0x8100000000000000ull
#define FLEXICAS_PFC_FLUSH         0x8200000000000000ull

#define FLEXICAS_PFC_QUERY         0x9000000000000000ull
#define FLEXICAS_PFC_CONGRU_TARGET 0x9100000000000000ull
#define FLEXICAS_PFC_CONGRU_QUERY  0x9200000000000000ull

#define FLEXICAS_PFC_EXTRACT_ADDR(cmd) \
  ((cmd & FLEXICAS_PFC_ADDR_SIGN) ? (FLEXICAS_PFC_ADDR_NEGF | (cmd & FLEXICAS_PFC_ADDR)) \
                                  : (cmd & FLEXICAS_PFC_ADDR))

#endif
